// if statement

let num = 8;
if(num%2 == 0){
    console.log('Even');
}
if(num%2 > 0){
    console.log('Odd');
}


// if-else statement

let num1 = 7;

if(num1%2 == 0){
    console.log('Even');
}
else{
    console.log('Odd');
}


// if-elseif-else statement

let num2 = 10;

if(num2 > 0){
    console.log('Positive');
}
else if(num2 < 0){
    console.log('Negative');
}
else{
    console.log('Zero');
}


